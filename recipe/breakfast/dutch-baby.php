                <h1>Dutch Baby</h1>
                
                <ul>
                    <li>vegetable oil <span>•</span> 2 T</li>
                    <li>all-purpose flour <span>•</span> 1 cup</li>
                    <li>cornstarch <span>•</span> 1/4 cup</li>
                    <li>lemon zest <span>•</span> 2 tsp</li>
                    <li>lemon juice <span>•</span> 2 T</li>
                    <li>salt <span>•</span> 1 tsp</li>
                    <li>eggs <span>•</span> 3 large</li>
                    <li>skim milk <span>•</span> 1 1/4 cup</li>
                    <li>butter <span>•</span> 1 T</li>
                    <li>vanilla extract <span>•</span> 1 tsp</li>
                    <li>confectioners’ sugar <span>•</span> 3 T</li>
                </ul>
                
                <ol>
                    <li>Adjust oven rack to middle position and heat oven to 450°. Brush surface and sides of large skillet with oil. Place skillet on oven rack and heat until oil is shimmering, about 10 minutes.</li>
                    <li>Meanwhile, combine flour, cornstarch, lemon zest, and salt in large bowl. Whisk eggs in another bowl until frothy and light, about 1 minute. Whisk milk, butter, and vanilla into eggs until incorporated. Whisk one-third of milk mixture into flour mixture until no lumps remain, then slowly whisk in remaining milk mixture until smooth.</li>
                    <li>Carefully pour batter into heated skillet and bake until edges of Dutch Baby are deep golden brown and crisp, about 20 minutes. Transfer skillet to wire rack and sprinkle Dutch Baby with lemon juice and confectioners’ sugar. Cut into wedges.</li>
                </ol>
                
                <div class="side-notes">
                    <p>Whole or lowfat milk is fine, but will result in a less crisp Dutch Baby. It will deflate after it sits a few minutes. Don't worry—it’s supposed to</p>
                    <p>Source: Cooks Country</p>
