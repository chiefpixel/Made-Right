                <h1>Blueberry Buttermilk Pancakes</h1>
                
                <ul>
                    <li>all-purpose flour <span>•</span> 1 1/3 cups</li>
                    <li>sugar <span>•</span> 3 T</li>
                    <li>baking powder <span>•</span> 2 1/2 tsp</li>
                    <li>salt <span>•</span> 3/4 tsp</li>
                    <li>buttermilk <span>•</span> 1 1/4 cups</li>
                    <li>eggs <span>•</span> 2 large</li>
                    <li>butter <span>•</span> 2 T melted + more for cooking</li>
                    <li>fresh blueberries <span>•</span> 2 cups</li>
                </ul>
                
                <ol>
                    <li>Preheat oven to 250°F and place baking sheet in oven. Whisk first 4 ingredients in large bowl. Whisk buttermilk, eggs, and 2 T butter in medium bowl; stir into dry ingredients. Fold in berries.</li>
                    <li>Heat large nonstick griddle or skillet over medium heat; brush with butter. Drop batter by 1/3 cupfuls onto griddle. Cook pancakes until brown, brushing griddle with more butter as needed, about 3 minutes per side. Transfer to sheet in oven to keep warm. Serve pancakes with maple syrup.</li>
                </ol>
                
