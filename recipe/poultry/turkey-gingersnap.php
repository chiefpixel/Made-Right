            <h1>Brined Turkey with Gingersnap Gravy</h1>
            
            <p>brine and turkey:</p>
            <ul>
                <li>turkey or turkey breast <span>•</span> 9-12 lbs</li>
                <li>water <span>•</span> 3 1/2 quarts (14 cups)</li>
                <li>coarse salt <span>•</span> 1 cup (4 1/2 oz)</li>
                <li>dark brown sugar <span>•</span> 1/2 cup packed</li>
                <li>molasses <span>•</span> 1/2 cup</li>
                <li>fresh thyme <span>•</span> 1 bunch, divided</li>
                <li>fresh sage <span>•</span> 1/2 bunch, divided</li>
                <li>ice cubes <span>•</span> 1 quart (4 cups)</li>
                <li>olive oil <span>•</span> 1 1/2 T</li>
                <li>ground black pepper <span>•</span> 1/2 T</li>
                <li>low-salt chicken broth <span>•</span> 3 cups</li>
                <li>onion <span>•</span> 1 large, halved</li>
                <li>garlic <span>•</span> 1/2 head, halved horizontally</li>
            </ul>
            
            <p>gravy:</p>
            <ul>
                <li>onion <span>•</span> 1 cup finely chopped</li>
                <li>fresh thyme <span>•</span> 2 tsp chopped</li>
                <li>gingersnap cookies <span>•</span> 4 1/2 oz* coarsely crumbled (1 3/4 cups)</li>
                <li>apple cider vinegar <span>•</span> 4 T</li>
                <li>Worcestershire sauce <span>•</span> 1 tsp</li>
            </ul>
            
            <p>brine:</p>
            <ol>
                <li>Line very large (about 8-quart) bowl with two 13-gallon plastic bags, one inside the other. Rinse turkey inside and out and place inside plastic-lined bowl.</li>
                <li>Combine water, salt, sugar, molasses, 1/2 bunch thyme, and 1/4 bunch sage in a second bowl. Stir until salt and sugar dissolve. Mix in ice cubes. Pour brine over turkey and gather tops of bags together, eliminating air space above brine; seal bags. Refrigerate turkey in brine 18 to 20 hours.</li>
            </ol>
            
            <p>rub:</p>
            <ol>
                <li>Stir oil, pepper, 1 1/2 tsp chopped thyme, and 1 1/2 tsp chopped sage in small bowl to form paste; smear all over outside of turkey.</li>
            </ol>
            
            <p>stock:</p>
            <ol>
                <li>Simmer chicken broth with turkey neck, heart, gizzard and liver (as well as optional 2 carrots, 2 celery stalks and 1/2 onion) until reduced to two cups. (Or just use the chicken broth in lieu of a stock.</li>
            </ol>
            
            <p>turkey:</p>
            <ol>
                <li>Set oven rack at lowest position and preheat to 350°. Remove turkey from brine. Drain well and discard brine. Pat turkey dry inside and out.</li>
                <li>Place turkey on small rack set in large roasting pan. Fill main cavity with remaining 1/2 bunch thyme (minus 2 tsp for gravy) and 1/4 bunch sage, onions, and garlic. Tent loosely with foil.</li>
                <li>Roast turkey 1 hour, turn pan around and roast 30 minutes. Pour 1/2 cup stock over turkey and re-tent with foil (foil should remain until about 1 hour cooking time left). Continue this procedure every 30 minutes until thermometer in thickest part registers 165°.</li>
                <li>Transfer turkey to platter. Remove vegetables and herbs from main cavity and discard. Spoon any juices from cavity into roasting pan. Let turkey stand 30 minutes (internal temperature will increase 5 to 10°).</li>
            </ol>
            
            <p>gravy:</p>
            <ol>
                <li>Strain pan juices into bowl. Spoon off fat, reserving 2 T. Heat reserved 2 T turkey fat in heavy large saucepan over medium-high heat. Add onion and thyme. Sauté until onion browns, about 10 minutes. Add turkey stock, gingersnaps, 4 T cider vinegar, and Worcestershire sauce. Add 2 cups degreased pan juices and bring to boil, whisking to dissolve gingersnaps.</li>
                <li>Reduce heat to medium-low and simmer until gravy thickens, about 4 minutes. Add water to thin, if needed. Important: whisk occasionally to prevent sticking to bottom! Season gravy to taste with salt and pepper</li>
            </ol>
            
            <p>Note: Options – you’ll want to get started a day ahead, because the turkey is brined for 18 to 20 hours. Stuffing this turkey is not recommended; the brine remaining in the meat may soak into the stuffing during roasting. Great, moist turkey! And Gravy!<br>*Gravy comes out too thick—try less ginger snaps.</p>
            <p>Source: Bon Appétit, November 2002</p>
    
