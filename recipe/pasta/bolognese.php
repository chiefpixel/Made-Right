            <h1>Ragù Alla Bolognese</h1>
            
            <ul>
                <li>extra virgin olive oil <span>•</span> 1/4 cup</li>
                <li>yellow onion <span>•</span> 1 small, peeled and minced</li>
                <li>celery <span>•</span> 1 rib, trimmed and minced</li>
                <li>carrot <span>•</span> 1/2 medium, peeled, trimmed and minced</li>
                <li>prosciutto di parma <span>•</span> 1 oz, finely chopped</li>
                <li>chicken livers <span>•</span> 2 medium, finely chopped</li>
                <li>ground chuck <span>•</span> 1 1/2 lbs</li>
                <li>dry white wine <span>•</span> 1/2 cup</li>
                <li>hot milk <span>•</span> 1 cup</li>
                <li>beef, veal, or chicken broth <span>•</span> 1 cup</li>
                <li>Italian plum tomato purée <span>•</span> one 28 oz can</li>
                <li>salt and ground black pepper to taste</li>
            </ul>
            
            <ol>
                <li>Heat oil in a large heavy-bottomed pot over medium heat. Add onions and cook, stirring frequently with a wooden spoon, until soft and translucent but not browned, about 3 minutes. Add celery and carrots and cook, stirring frequently, for 3 minutes more. Add prosciutto and chicken livers and cook, stirring, until livers are just cooked and still a little pink, about 1 minute. Add ground chuck, season to taste with salt and pepper, and cook, breaking up meat with the back of the spoon, until just cooked and still a little pink, about 5 minutes more. (To keep meat tender, do not fry or brown.)</li>
                <li>Add wine to pot and cook, stirring, until it has evaporated, about 3 minutes. Reduce heat to medium-low, add hot milk, and cook, stirring occasionally, until milk has evaporated, about 10 minutes.</li>
                <li>Meanwhile, heat broth and tomato purée together in a medium saucepan over medium-high heat until hot, then add to meat mixture in pot. Reduce heat to low and gently simmer, stirring occasionally, for 2 1/2 hours. Season to taste with salt and pepper.</li>
            </ol>
            
            <p>Source: Saveur, September/October 2001</p>
            
