                    <li>sugar <span>•</span> 1 1/4 cup</li>
                    <li>water <span>•</span> 2 1/2 cup</li>
                    <li>lemon zest <span>•</span> 1/2 tsp</li>
                    <li>lemon juice <span>•</span> 1 1/4 cup</li>
                    <li>jalapeño <span>•</span> 1 large, stemmed and sliced</li>
                </ul>
                
                <ol>
                    <li>Start by making a lemonade base: Combine sugar and water in saucepan over medium heat and simmer until sugar is thoroughly dissolved. Allow to cool.</li>
                    <li>Add lemon juice, zest and jalapeño and pour into a large jar. Refrigerate 3 hours, then remove jalapeño (or leave in longer for spicier drink).</li>
                    <li>To serve, mix equal parts base and cold water into glass filled with crushed ice.  Makes about 4 cups of base.</li>
                </ol>
                
                <div class="side-notes">
