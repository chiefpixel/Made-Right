            <h1>Split Pea Soup with Caraway</h1>
            
            <ul>
                <li>caraway seeds <span>•</span> 1 1/2 tsp</li>
                <li>smoked bone-in picnic ham <span>•</span> 2 1/2 lbs</li>
                <li>bay leaves <span>•</span> 4</li>
                <li>split peas, rinsed and picked through <span>•</span> 1 lb</li>
                <li>olive oil <span>•</span> 2 T</li>
                <li>onions <span>•</span> 2 medium, chopped</li>
                <li>carrots <span>•</span> 2 medium, chopped</li>
                <li>celery <span>•</span> 2 stalks, chopped</li>
                <li>unsalted butter <span>•</span> 1 T</li>
                <li>garlic <span>•</span> 2 cloves, minced</li>
                <li>pinch granulated sugar</li>
                <li>new potatoes <span>•</span> 3 small, cut into 1/2-inch (about 3/4 cup)</li>
                <li>ground black pepper to taste</li>
                <li>red onion, minced (optional)</li>
                <li>balsamic vinegar to taste</li>
            </ul>
            
            <ol>
                <li>Toast caraway seeds in small skillet over medium-high heat, stirring frequently, until fragrant and browned, about 4 minutes.</li>
                <li>Place ham, bay leaves, and 3 quarts water in Dutch oven. Cover and bring to a boil over medium-high heat. Reduce heat to low and simmer until meat is tender and pulls away from bone, 2 to 2 1/2 hours. Remove ham meat and bone from pot and set aside.</li>
                <li>Add split peas and toasted caraway seeds to stock. Bring back to boil, reduce heat, and simmer, uncovered, until peas are tender but not dissolved, about 45 minutes. Meanwhile, when ham is cool enough to handle, shred meat into bite-sized pieces and set aside. Discard rind and bone.</li>
                <li>While ham is simmering, heat oil in large skillet over high heat until shimmering. Add onions, carrots, and celery; sauté, stirring frequently, until most of the liquid evaporates and vegetables begin to brown, 5 to 6 minutes. Reduce heat to medium low; add butter, garlic, and sugar. Cook vegetables, stirring frequently, until deeply browned, 30 to 35 minutes; set aside.</li>
                <li>Add sautéed vegetables, potatoes, and shredded ham to soup; simmer until potatoes are tender and peas dissolve and thicken soup to the consistency of light cream, about 20 minutes more. Season with ground black pepper. Ladle soup into bowls, sprinkle with red onion, if using, and serve, passing balsamic vinegar separately.</li>
            </ol>
            
            <p>Note: To cut 45 minutes off the cooking time of the soup, simmer the ham 1 1/2 hours, then add the split peas to the pot. When the ham is tender, after about 45 minutes of simmering, remove it and shred; continue with the recipe.</p>
