            <h1>Anasazi Squash Soup</h1>
            
            <ul>
                <li>kidney beans <span>•</span> 3/4 cup dried</li>
                <li>chorizo sausage <span>•</span> 1 lb, casings removed and sliced 1/4-inch</li>
                <li>onion <span>•</span> 1 large, chopped</li>
                <li>garlic <span>•</span> 2 cloves, minced</li>
                <li>water <span>•</span> 3 cups</li>
                <li>beef broth <span>•</span> 4 cups</li>
                <li>butternut squash <span>•</span> 3 cups 1/2-inch cubed</li>
                <li>red bell pepper <span>•</span> 1 small, finely chopped</li>
                <li>corn <span>•</span> 1 1/4 cup</li>
                <li>pepitas (pumpkin seeds) <span>•</span> 1/2 cup shelled</li>
                <li>pepper and salt to taste</li>
            </ul>
            
            <ol>
                <li>In medium bowl, place kidney beans with water to cover by 3″ and let sit over night. Drain.</li>
                <li>In dry frying pan, toast pepitas and set aside. Sauté chorizo in heavy pot over medium heat until cooked through and fat is rendered. Transfer chorizo to paper towels and leave 2 T of drippings in pot. Add onion and garlic; sauté until tender. Add 3 cups water, broth and drained beans; bring to boil. Simmer at low heat for 1 hour.</li>
                <li>Add squash to soup and simmer until tender. Stir in bell peppers, corn and chorizo and simmer an additional ten minutes.</li>
                <li>Meanwhile, set aside 2 T pepitas for garnish and finely grind remaining seeds in blender. Stir the ground pepitas into soup.</li>
            </ol>
            
            <p>Note: Top soup with sour cream or cilantro.</p>
            <p>Source: Bon Appétit, October 1999</p>
