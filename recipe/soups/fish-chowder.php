            <h1>Fish Chowder</h1>
            
            <ul>
                <li>good bacon <span>•</span>  4 to 6 slices (about 1/4 lb)</li>
                <li>onion <span>•</span>  1 cup minced</li>
                <li>baking potatoes <span>•</span>  2 cups peeled and roughly chopped</li>
                <li>flour <span>•</span>  2 T</li>
                <li>fresh thyme leaves <span>•</span>  1 tsp (or 1/2 tsp dried)</li>
                <li>chicken or fish stock <span>•</span>  2 cups</li>
                <li>salt and pepper <span>•</span>  to taste</li>
                <li>milk <span>•</span>  1 cup</li>
                <li>heavy cream <span>•</span>  1 cup</li>
                <li>white fish <span>•</span>  2 cups diced or chunked</li>
                <li>butter <span>•</span>  1 T</li>
                <li>optional: minced fresh parsley leaves for garnish</li>
            </ul>
            
            <ol>
                <li>Fry the bacon in a large, deep saucepan over medium-high heat until crisp. Remove with a slotted spoon and cook the onion and potatoes in the bacon fat until the onion softens, 10 minutes.</li>
                <li>Sprinkle with optional flour and thyme and stir. Add the stock and cook until the potatoes are tender, about 10 minutes.</li>
                <li>Add salt and pepper, then the milk and cream; add the fish and bring barely to a simmer over low heat. Float the butter on top of the chowder; by the time it melts, the fish will be ready. Garnish and serve.</li>
            </ol>
            
