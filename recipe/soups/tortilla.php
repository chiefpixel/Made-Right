            <h1>Tortilla Soup</h1>
            
            <p>tortilla strips:</p>
            <ul>
                <li>corn tortillas <span>•</span> 8 6″, cut into 1/2-inch wide strips</li>
                <li>vegetable oil <span>•</span> 1 T</li>
                <li>salt</li>
            </ul>
            
            <p>soup:</p>
            <ul>
                <li>chicken breasts <span>•</span> 2 split bone-in, skin-on (about 1 1/2 lbs)</li>
                <li>chicken broth <span>•</span> 8 cups</li>
                <li>white onion <span>•</span> 1 very large (about 1 lb), quartered</li>
                <li>garlic <span>•</span> 4 cloves</li>
                <li>fresh epazote <span>•</span> 2 sprigs (or 10 sprigs fresh cilantro + 1 sprig fresh oregano)</li>
                <li>tomatoes <span>•</span> 2 medium, quartered</li>
                <li>jalapeño <span>•</span> 1 medium</li>
                <li>chile en adobo <span>•</span> 1 chile plus 1 T adobo</li>
                <li>vegetable oil <span>•</span> 1 T</li>
            </ul>
            
            <p>garnishes:</p>
            <ul>
                <li>lime, avocado, cotija cheese, cilantro, jalapeño, Mexican crema or sour cream</li>
            </ul>
            
            <p>for the tortilla strips:</p>
            <ol>
                <li>Adjust oven rack to middle position; heat oven to 425°. Spread tortilla strips on rimmed baking sheet; drizzle with oil and toss until evenly coated. Bake until strips are deep golden brown and crisped, about 14 minutes, rotating pan and shaking strips (to redistribute) halfway through baking time.</li>
                <li>Season strips lightly with salt; transfer to plate lined with several layers paper towel.</li>
            </ol>
            
            <p>for the soup:</p>
            <ol>
                <li>While tortilla strips bake, bring broth, 2 onion quarters, 2 garlic cloves, epazote, and 1/2 tsp salt to boil over medium-high heat in large saucepan; Add chicken and simmer uncovered for 5 minutes. Remove pan from heat and cover. Let stand for 10 minutes or until chicken is cooked.</li>
                <li>Using tongs, transfer chicken to large plate. Pour broth through fine-mesh strainer; Discard solids in strainer. When cool enough to handle, shred chicken into bite-sized pieces; Discard bones.</li>
                <li>Puree tomatoes, 2 remaining onion quarters, 2 remaining garlic cloves, jalapeño, chipotle chile and 1 tsp adobo sauce in food processor until smooth.</li>
                <li>Heat oil in Dutch oven over high heat until shimmering; Add tomato/onion puree and 1/8 tsp salt and cook, stirring frequently, until mixture has darkened in color, about 10 minutes. Stir strained broth into tomato mixture, bring to boil, then reduce heat to low and simmer to blend flavors, about 15 minutes.</li>
                <li>Taste soup and add up to 2 tsp additional adobo sauce if desired. Add shredded chicken and simmer until heated through, about 5 minutes. To serve, place portions of tortilla strips in bottom of individual bowls and ladle soup into bowls. Pass garnishes separately.</li>
            </ol>
                        
