<li>1/4 ounce dried shiitake mushrooms, rinsed</li>
<li>4 1/4 cups water</li>
<li>1 sprig fresh thyme</li>
<li>1 bay leaf</li>
<li>1 garlic clove, peeled, plus 4 cloves, minced</li>
<li>Salt and pepper</li>
<li>1/4 teaspoons baking soda</li>
<li>1 cup wild rice</li>
<li>4 tablespoons unsalted butter</li>
<li>1 pound cremini mushrooms, trimmed and sliced 1/4 inch thick</li>
<li>1 onion, chopped fine</li>
<li>1 teaspoon tomato paste</li>
<li>2/3 cup dry sherry</li>
<li>4 cups low-sodium chicken broth</li>
<li>1 tablespoon soy sauce</li>
<li>1/4 cup cornstarch</li>
<li>1/2 cup heavy cream</li>
<li>1/4 cup minced fresh chives</li>
<li>1/4 teaspoon finely grated lemon zest</li>
            </ul>
            
            <ol>


<li>Adjust oven rack to middle position and heat oven to 375°. Grind shiitake mushrooms in spice grinder until finely ground (you should have about 3 tablespoons).</li>
<li>Bring 4 cups water, thyme, bay leaf, garlic clove, 3/4 teaspoon salt, and baking soda to boil in medium saucepan over high heat. Add rice and return to boil. Cover saucepan, transfer to oven, and bake until rice is tender, 35 to 50 minutes. Strain rice through fine-mesh strainer set in 4-cup liquid measuring cup; discard thyme, bay leaf, and garlic. Add enough water to reserved cooking liquid to measure 3 cups.</li>
<li>Melt butter in Dutch oven over high heat. Add cremini mushrooms, onion, minced garlic, tomato paste, 3/4 teaspoon salt, and 1 teaspoon pepper. Cook, stirring occasionally, until vegetables are browned and dark fond develops on bottom of pot, 15 minutes. Add sherry, scraping up any browned bits, and cook until reduced and pot is almost dry, about 2 minutes. Add ground shiitake mushrooms, reserved rice cooking liquid, broth, and soy sauce and bring to boil. Reduce heat to low and simmer, covered, until onion and mushrooms are tender, about 20 minutes.</li>
<li>Whisk cornstarch and remaining 1/4 cup water in small bowl. Stir cornstarch slurry into soup, return to simmer, and cook until thickened, about 2 minutes. Remove pot from heat and stir in cooked rice, cream, chives, and lemon zest. Cover and let stand for 20 minutes. Season with salt and pepper to taste, and serve.</li>

