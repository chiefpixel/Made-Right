            <h1>Drunken Pintos</h1>
            
            <ul>
                <li>dry pinto beans <span>•</span> 8 oz (1 1/4 cups)</li>
                <li>cubed pork shoulder <span>•</span> 2 oz (or extra chopped bacon instead)</li>
                <li>thick slices bacon <span>•</span> 4 thick slices, cut into 1/2-inch pieces</li>
                <li>white onion <span>•</span> 1 small, 1/4-inch dice</li>
                <li>fresh serranos or jalapeños <span>•</span> 2 medium, seeded and sliced</li>
                <li>salt <span>•</span> 3/4 tsp</li>
                <li>tequila <span>•</span> 1 1/2 T (plus a little more if you like)</li>
                <li>cilantro <span>•</span> 1/4 cup roughly chopped</li>
            </ul>
            
            <ol>
                <li>Rinse the beans and scoop into a 4-quart Dutch oven. Add 5 cups water, remove any beans that float, then add the pork shoulder and bring to a boil. Reduce the heat and very gently simmer, partially covered, until the beans are thoroughly tender, about 2 hours. You need to gently stir the beans regularly and add water as necessary to keep the liquid a generous 1/2-inch above the beans.</li>
                <li>In a medium-size skillet, fry the bacon until crisp. Remove the bacon with a slotted spoon, leaving behind the drippings; pour off all but 2 T and return the pan to medium heat. Add the onion and chiles and fry until deep golden brown, about 10 minutes. Scrape the onion mixture into the beans, then taste and season with salt. Continue simmering, stirring occasionally, for 20 to 30 minutes to blend the flavors.</li>
                <li>If the beans seem quite soupy, boil over medium-high heat, stirring frequently, until the consistency of a nice, brothy bean soup. (An alternative here is to puree 1/4 of the beans in a food processor or blender, returning them to the pot to thicken the broth.)  Just before serving, stir in the tequila and cilantro, then serve in warm bowls topped with the crumbled bacon.</li>
            </ol>
            
            <p>Notes: For advance prep, recipe can be followed  through step 2 several days ahead. As shortcut, two 16-ounce cans of pinto beans can replace the beans in step 1.</p>
