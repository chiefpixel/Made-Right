            <h1>Green Poblano Rice</h1>
        
            <ul>
                <li>chicken broth or water <span>•</span> 1 1/2 cups</li>
                <li>fresh poblano chiles <span>•</span> 2 medium, seeded and chopped </li>
                <li>cilantro <span>•</span> 12 sprigs, plus extra for garnish</li>
                <li>salt <span>•</span> 1/2 tsp if salted broth, 1 tsp if unsalted broth</li>
                <li>canola or olive oil <span>•</span> 1 T</li>
                <li>rice <span>•</span> 1 cup medium grain (preferably)</li>
                <li>white onion <span>•</span> 1 small, 1/4-inch dice</li>
                <li>garlic <span>•</span> 5 cloves, finely chopped</li>
            </ul>
            
            <ol>
                <li>Combine the chiles and broth in 2-qt saucepan. Bring to a boil, then simmer, partially covered, until the chiles are very soft, about 10 minutes. Pour the mixture into a food processor, add the cilantro (stems and all), and process to a smooth puree. Press through a medium-mesh strainer into a bowl and stir in the salt.</li>
                <li>Wipe the pan clean, add the oil and heat over medium. Add the rice and onion, and cook, stirring regularly, until the rice is chalky looking and the onion is soft, about 5 minutes. Stir in the garlic and cook a minute longer.</li>
                <li>Add the warm (or reheated) chile liquid to the hot rice pan, stir once, scrape down any rice kernels clinging to the side of the pan, cover, and cook over medium-low heat for 15 minutes. Uncover and check a grain of rice: It should be nearly cooked through. Turn off the heat and let stand covered for 10 minutes longer. Fluff with a fork, scoop into a warm serving dish, garnish with cilantro sprigs.</li>
            </ol>
        
            <p>Notes: Variations: grilled corn cut from 1 cob or 1 large grilled zucchini (cubed) are tasty vegetable add-ins. About 1 cup coarsely shredded roast (or barbecued) pork or smoked salmon, mixed in toward the end of cooking, will make green rice a full meal.</p>
            <p>Source: Rick Bayless’s Mexican Kitchen</p>
            
