            <h1>Fresh Fruit with Tequila-Lime Syrup</h1>
            
            <ul>
                <li>sugar <span>•</span> 1 cup</li>
                <li>water <span>•</span> 2 cups</li>
                <li>fresh mint <span>•</span> 2 bunches</li>
                <li>tequila <span>•</span> 1/4 cup</li>
                <li>lime juice <span>•</span> 2 T</li>
                <li>fresh pineapple <span>•</span> 1 cup diced</li>
                <li>banana <span>•</span> 1 medium, sliced 1/4-inch</li>
                <li>fresh strawberries <span>•</span> 1/2 cup hulled and quartered</li>
                <li>plain low-fat yogurt <span>•</span> 1 1/2 cups</li>
            </ul>
            
            <ol>
                <li>Place sugar, water and one bunch of mint in a medium saucepan and bring to a boil. Reduce heat and simmer for 15 minutes. Add the tequila and simmer 5 minutes more. Turn off the heat and add lime juice. Cool and strain to remove mint.</li>
                <li>Place the pineapple, banana and strawberries in a bowl. Coarsely chop the leaves from the remaining bunch of mint and sprinkle over the fruit, stirring to combine. Pour the syrup over the fruit and let the mixture stand for 15 minutes. Serve topped with 1/4 cup yogurt.</li>
            </ol>
            
