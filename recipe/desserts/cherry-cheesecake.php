            <h1>Cherry-Topped Cheesecake</h1>
            
            <ul>
                <li>Duncan Hines Yellow Cake mix <span>•</span> 1 box</li>
                <li>eggs <span>•</span> 4 large</li>
                <li>vegetable oil <span>•</span> 2 T</li>
                <li>cream cheese <span>•</span> 16 oz (2 packages), softened</li>
                <li>sugar <span>•</span> 1/2 cup</li>
                <li>milk <span>•</span> 1 1/2 cups</li>
                <li>lemon juice <span>•</span> 3 T</li>
                <li>vanilla <span>•</span> 1 T</li>
                <li>cherry pie filling <span>•</span> 1 can (21 oz)</li>
            </ul>
            
            <ol>
                <li>Preheat oven to 300°. Reserve 1 cup dry cake mix and set aside. In large bowl, stir together remaining dry cake mix, 1 egg, and oil. Mixture will be crumbly. Press crumb mixture into bottom and 3/4 up sides of greased 13″ x 9″ pan.</li>
                <li>In same bowl, blend cream cheese and sugar. Add 3 eggs and reserved cake mix; beat 1 minute on medium speed. At low speed, gradually add milk, lemon juice, and vanilla extract and mix till smooth.</li>
                <li>Pour into crust. Bake at 300° for 45 to 55 minutes, or till center is firm. When cool, top with pie filling. Chill before serving.</li>
            </ol>
            
