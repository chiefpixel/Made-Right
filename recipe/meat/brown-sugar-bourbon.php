    <h1>Brown Sugar Bourbon Steak Tips</h1>

    <ul>
        <li>rye or bourbon <span>•</span> 1/2 cup</li>
        <li>brown sugar <span>•</span> 1/2 cup</li>
        <li>Worcestershire sauce <span>•</span> 1 tsp</li>
        <li>soy sauce <span>•</span> 1/4 cup</li>
        <li>yellow onion <span>•</span> 1 medium, cut into 1/4-inch thick rings</li>
        <li>beef sirloin steak tips or flank steak <span>•</span> 2 lbs</li>
    </ul>
    
    <ol>
        <li>In a medium-size bowl, whisk together bourbon, brown sugar, Worcestershire and soy sauce. Pour into a large zip-top bag and add onion and steak. Seal, squeezing to remove air, and let meat marinate for 2 hours or more.</li>
        <li>Remove the tips and onions and pour marinade into saucepan and heat until boiling. Set aside.</li>
        <li>On a hot grill, sear both sides of the steak tips then move to cooler side to cook to desired doneness. Grill onions. Pour marinade over meat before serving.</li>
    </ol>
    
