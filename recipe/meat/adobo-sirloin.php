    <h1>Adobo Sirloin</h1>

    <ul>
        <li>lime <span>•</span> 1 medium, juiced</li>
        <li>garlic <span>•</span> 1 T</li>
        <li>oregano <span>•</span> 1 tsp</li>
        <li>ground cumin <span>•</span> 1 tsp</li>
        <li>canned chipotles in adobo sauce <span>•</span> 2 T finely chopped</li>
        <li>beef sirloin steaks <span>•</span> four 8 oz</li>
        <li>adobo sauce from canned chipotle peppers to taste</li>
        <li>salt and pepper to taste</li>
    </ul>
    
    <ol>
        <li>In a small bowl, mix the lime juice, garlic, oregano and cumin. Stir in chipotle peppers, and season to taste with adobo sauce.</li>
        <li>Pierce the meat on both sides with a sharp knife, sprinkle with salt and pepper, and place in a glass dish. Pour lime and chipotle sauce over meat, and turn to coat. Cover and marinate in the refrigerator for 1 to 2 hours.</li>
        <li>Preheat grill of broiler for high heat. Lightly brush grill grate with oil. Place steaks on the grill, and discard marinade. Grill steaks for 6 minutes per side, or to desired doneness.</li>
    </ol>
    
